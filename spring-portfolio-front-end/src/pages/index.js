import Head from 'next/head'
import Link from 'next/link'
import Footer from '../../components/footer-component'
import Navbar from '../../components/navbar'
import ListInvestments from '../../components/list-investments'
import FundsDisplay from '../../components/funds-display'
import AddInvestment from '../../components/add-investment'
import DeleteInvestments from '../../components/delete-investments'
import { useState, useEffect } from 'react'
import initializeCheckboxes from '../../lib/initializeCheckboxes'
import compareNamesAscending from '../../lib/compareNameAscending'
import compareNamesDescending from '../../lib/compareNameDescending'
import compareFundsAscending from '../../lib/compareFundsAscending'
import compareFundsDescending from '../../lib/compareFundsDescending'

export async function getInvestments() {
  const res = await fetch(process.env.NEXT_PUBLIC_INVESTMENT_BASE_URL);
  const investments = await res.json();
  return investments; 
}

export async function getRemainingFunds() {
  const res = await fetch(process.env.NEXT_PUBLIC_FUNDS_BASE_URL + "/" + 1);
  const remainingFunds = await res.json();
  return remainingFunds;
}

export default function Home() {
  const [investments, setInvestments] = useState([]);
  const [checkboxes, setCheckboxes] = useState([]);
  const [selectAll, setSelectAll] = useState(false);
  const [currentFunds, setCurrentFunds] = useState([]);
  useEffect(() => {
    getInvestments().then((data) =>  {
        setInvestments(data);
        setCheckboxes(initializeCheckboxes(data));
      }
    );
    getRemainingFunds().then((fetchedFunds) => {
      setCurrentFunds(fetchedFunds.funds);
    })
  },[])
  const noBoxesChecked = checkboxes.every((checkbox) => checkbox.checked == false);
  const selectedInvestments = checkboxes.filter((checkbox) => checkbox.checked == true);
  const selectedIds = selectedInvestments.map((selected) => selected.id);

  const updateCheckboxesHandler = (newCheckboxes) => {
    setCheckboxes(newCheckboxes);
  }

  const updateCurrentFundsHandler = (updatedCurrentFunds) => {
    const newAvailableFunds = parseInt(currentFunds) + parseInt(updatedCurrentFunds);
    setCurrentFunds(newAvailableFunds);
  }

  const sortNameAscending = (investments) => {
    const sorted = investments.sort(compareNamesAscending);
    setCheckboxes(initializeCheckboxes(sorted));
  }

  const sortNameDescending = (investments) => {
    const sorted = investments.sort(compareNamesDescending);
    setCheckboxes(initializeCheckboxes(sorted));
  }

  const sortFundsAscending = (investments) => {
    const sorted = investments.sort(compareFundsAscending);
    setCheckboxes(initializeCheckboxes(sorted));
  }

  const sortFundsDescending = (investments) => {
    const sorted = investments.sort(compareFundsDescending);
    setCheckboxes(initializeCheckboxes(sorted));
  }

  return (
    <>
      <Head>
        <title>My Spring Portfolio</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Navbar brandName={"My Spring Portfolio"} notificationName={"Notifications"} numAlerts={0}/>
      <FundsDisplay funds={currentFunds} currency={"USD"} updateFunds={updateCurrentFundsHandler}/>
      <div className="container text-center">
        <div className="badge bg-dark fs-1">Investments</div>
        <div className="d-flex">
          <AddInvestment buttonClass="me-auto btn btn-primary" checkboxes={checkboxes} updateCheckboxes={updateCheckboxesHandler} updateFunds={updateCurrentFundsHandler}/>
          <button className="btn btn-light" onClick={() => {sortNameAscending(investments)}}>Sort Names (Ascending)</button>
          <button className="btn btn-light" onClick={() => {sortNameDescending(investments)}}>Sort Names (Descending)</button>
          <button className="btn btn-light" onClick={() => {sortFundsAscending(investments)}}>Sort Funds (Ascending)</button>
          <button className="btn btn-light" onClick={() => {sortFundsDescending(investments)}}>Sort Funds (Descending)</button>
          <button type="button" className="btn btn-primary" disabled={noBoxesChecked}>
            <Link href={`/view-selected-investments/${selectedIds}`}>
              View
            </Link>
          </button>
          <DeleteInvestments checkboxes={checkboxes} updateCheckboxes={updateCheckboxesHandler} updateFunds={updateCurrentFundsHandler}></DeleteInvestments>
        </div>
        <ListInvestments selectAll={selectAll} setSelectAll={setSelectAll} checkboxes={checkboxes} setCheckboxes={setCheckboxes}/>
      </div>
      <Footer footerText={"Created by Ricky"}/>
    </>
  )
}
